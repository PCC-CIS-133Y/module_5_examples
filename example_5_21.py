# we can use reverse() on our sandwich list and our price list
sandwiches = ["chipotle chicken", "reuben", "ham and cheese", "meatball", "monte cristo", "roast beef"]

sandwiches.reverse()
print(sandwiches)

# and we can use reverse() and our price list

prices = [10, 11, 12, 13, 14, 15]

prices.reverse()
print(prices)